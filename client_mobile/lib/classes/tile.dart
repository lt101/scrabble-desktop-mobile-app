class Tile {
  Tile(this.x, this.y, this.value, this.index, this.Multiplier, this.color,
      this.available);

  final int x;
  final int y;
  final String value;
  final int index;
  final String Multiplier;
  final String color;
  final bool available;

  String get letter => letter;
}
